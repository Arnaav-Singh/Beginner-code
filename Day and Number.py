

weekday = int(input("Enter weekday day number (1-7) : "))

if weekday == 1 :
    print("Sunday");

elif weekday == 2 :
    print("Monday")

elif(weekday == 3) :
    print("Tuesday")

elif(weekday == 4) :
    print("Wednessday")

elif(weekday == 5) :
    print("Thrusday")

elif(weekday == 6) :
    print("Friday")

elif (weekday == 7) :
    print("Saturday")

else :
    print("Please enter weekday number between 1-7.")